# break and continious statement

# Break Statement remove from the loop

# example:

for i in range(12):
    if(i == 10):
        break
    print("5x", i+1, "=", 5* (i+1))



# continue statement

# continue statement skip the iteration.

for i in range(12):
    if(i == 10):
        print("skip the iteration")
        continue
    print("5x", i+1, "=", 5* (i+1))


