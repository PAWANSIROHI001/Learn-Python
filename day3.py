# variables
# variable like a container that holds data
# variable can be of different types like string, integer, float, boolean, etc.

# e.g:

a= 2026
print(a)

b= "sirohi"
print(b)

c= 3.14
print(c)    

d= True
print(d)

# we can also find the type of variable by using type() function
print("type of a is", type(a)) # <class 'int'>
print("type of b is", type(b)) # <class 'str'>
print("type of c is", type(c)) # <class 'float'>
print("type of d is", type(d)) # <class 'bool'>

