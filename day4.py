# Today i will learn about operators in python.
# Operators are used to perform operations on variables and values.
# Python divides the operators in the following groups: 
# 1. Arithmetic operators
# 2. Assignment operators
# 3. Comparison operators
# 4. Logical operators
# 1. Arithmetic operators are used to perform mathematical operations like addition, subtraction, multiplication, etc.
# The arithmetic operators are: +, -, *, /, %, **, //
# example: 

a = 10
b = 5   
print(a + b)  # Output: 15
print(a - b)  # Output: 5   
print(a * b)  # Output: 50
print(a / b)  # Output: 2.0
print(a % b)  # Output: 0
print(a ** b) # Output: 100000
print(a // b) # Output: 2

# 2. Assignment operators are used to assign values to variables. The assignment operators are: =, +=, -=, *=, /=, %=, **=, //=
# example:

x = 10
x += 5  # This is equivalent to x = x + 5
print(x)  # Output: 15
x -= 3  # This is equivalent to x = x - 3
print(x)  # Output: 12

# 3. Comparison operators are used to compare two values. The comparison operators are: ==, !=, >, <, >=, <=
# example:

y = 20
print(y == 20)  # Output: True
print(y != 20)  # Output: False
print(y > 15)   # Output: True
print(y < 25)   # Output: True
print(y >= 20)  # Output: True
print(y <= 15)  # Output: False

# 4. Logical operators are used to combine conditional statements. The logical operators are: and, or, not
# example:

a = True
b = False   
print(a and b)  # Output: False
print(a or b)   # Output: True
print(not a)    # Output: False
print(not b)    # Output: True


